package dev.vivienhuang.mavenwebdemo.linebot.message.flex;

public class BubbleStyle {
	private BlockStyle header;
	private BlockStyle hero;
	private BlockStyle body;
	private BlockStyle footer;
	public BlockStyle getHeader() {
		return header;
	}
	public void setHeader(BlockStyle header) {
		this.header = header;
	}
	public BlockStyle getHero() {
		return hero;
	}
	public void setHero(BlockStyle hero) {
		this.hero = hero;
	}
	public BlockStyle getBody() {
		return body;
	}
	public void setBody(BlockStyle body) {
		this.body = body;
	}
	public BlockStyle getFooter() {
		return footer;
	}
	public void setFooter(BlockStyle footer) {
		this.footer = footer;
	}
}
